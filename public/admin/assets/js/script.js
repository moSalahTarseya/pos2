$(document).ready(function () {
       // Event In Back ( Check All Box )
    if($('#btn-delete-all').css('display', 'none')){
        $('#CheckAll').prop('checked', false);
        $('.selectedBox').prop('checked', false);
    }

    $('#CheckAll').on('click', function() {
        if($(this).prop('checked') === true) {
            $('.selectedBox').prop('checked', true);
            if ($('.selectedBox').filter(':checked').length > 0) {
                $('#btn-delete-all').css('display', 'block');
                $('#btn-restore-all').css('display', 'block');
            }
        } else {
            $('.selectedBox').prop('checked', false);
            $('#btn-delete-all').css('display', 'none');
            $('#btn-restore-all').css('display', 'none');
        }
    });

    // Start Option
    if($('#option_id').length) {
        // Questions Section
        var NoValidation = $('#answersValidation').data('content');
        var CountOptions = $('#OldOptionID').data('content');
        // Check Answer Type
        var AnswerType = $('#AnswerType').data('content');
        AnswerType = AnswerType ? AnswerType : 'checkbox';
        var CorrectTypeClass = AnswerType === 'radio' ? 'correctChecked' : '';

        if (CountOptions !== '') {
            GetOptions(CountOptions, NoValidation, 'new', 1, CountOptions)
        }

        // Change Option ID
        $(document).on("change", "#option_id", function () {
            const count = $('#answers .answerTitle').length;
            const ValOptions = $(this).val() - count;
            if (ValOptions > 0) {
                // Add New
                GetOptions(ValOptions, null, 'new', count, Math.abs(ValOptions + count));
            } else {
                // Remove Old
                GetOptions(ValOptions, null, 'old');
            }
        });

        var type;
        $('#question_type').change(function () {
            $('#type_q').val(this.value)
            $(this).attr('disabled', 'disabled');

            //var max;
            //var corr;
            if (this.value == 2) {
                type = 'checkbox';
                max = 10;
            } else if (this.value == 3) {
                type = 'radio';
                // corr = 'correctChecked';
                // max = 2;
            } else {
                type = 'radio';
                // max = 10;
                // corr = 'correctChecked';
            }
        });

        // Checking Func
        function GetOptions(numOptions, validation=null, status, main=1, plus=0) {
            const ANSWERS = $('#answers');
            var Checked = false;
            var Answer = '';



            if (status === 'new') {
                var plusN = main !== 1 ? main + 1 : main;
                for (var i = plusN; i <= plus; i++) {
                    if (validation !== null && Object.keys(validation).length) {
                        Answer = validation[i].answer !== null ? validation[i].answer : "";
                        Checked = validation[i].correct === '1' ? "checked" : "";
                    }


                    var html = '<div class="row">';
                    html += '<div class="col-md-10"><div class="form-group d-rtl">';
                    html += '<label for="answer_' + i + '">الأجابة <span class="required">*</span></label>';
                    html += '<input type="text" id="answer_' + i + '" name="answers[' + i + '][answer]" value="' + Answer + '" class="form-control answerTitle" placeholder="أدخل الإجابة">'
                    html += '</div></div>';
                    // html += '<div class="col-md-5"><div class="form-group d-ltr">';
                    // html += '<label for="answer_' + i + '">Answer Is In English<span class="required">*</span></label>';
                    // html += '<input type="text" id="answer_' + i + '" name="answers[' + i + '][answer]" value="' + Answer + '" class="form-control answerTitle" placeholder="Enter Answer Is In English">'
                    // html += '</div></div>';
                    html += '<div class="col-md-2"><div class="form-group">';
                    html += '<label for="correct' + i + '"> الاجابة الصحيحة </label>';
                    html += '<input type="'+ type +'" id="correct' + i + '" name="answers[' + i + '][correct]" ' + Checked + ' value="1" class="form-control ' + CorrectTypeClass + '">';
                    html += '</div></div></div>';
                    ANSWERS.append(html);
                }
            } else if (status === 'old') {
                $('#answers > .row').slice(numOptions).remove();
            }
        }

        // Submit Form Questions
        function submitForm() {
            $('#submitQuestion').click(function (e) {
                e.preventDefault();
                const AnswersTitle = $('#answers .answerTitle');
                AnswersTitle.filter(function () {
                    if ($(this).val().match(/^\s*$/)) {
                        $(this).parent().parent().parent().remove();
                        AnswersTitle.length--;
                    }
                });
                if (AnswersTitle.length) {
                    $('#OldOptionID, #option_id').val(AnswersTitle.length);
                    $('#option_id option[value="' + AnswersTitle.length + '"]').attr('selected', 'selected');
                    if (($('#answers input[type=radio]:checked').length >= 1) || ($('#answers input[type=checkbox]:checked').length >= 1)) {
                        $('#storeQuestion').submit();
                    } else {
                        alert('الرجاء إختيار إجابة صحيحة علي الآقل')
                    }
                } else {
                    $('#option_id option:first').prop('selected', true);
                    alert('الرجاء أدخل إجابة واحدة علي الآقل')
                }
            })
        }

        submitForm();
    }
    // End Option

    const Checkbox = $('.selectedBox');
    Checkbox.change(function (){
        if ($('.selectedBox').filter(':checked').length > 0){
            $('#btn-delete-all').css('display', 'block');
            $('#btn-restore-all').css('display', 'block');
        }else {
            $('#btn-delete-all').css('display', 'none');
            $('#btn-restore-all').css('display', 'none');
            $('#CheckAll').prop('checked', false);
        }
    })


    // Check All General Site
    $('#check_all_general_site').click(function (){
        if($(this).prop('checked') === true){
            $('.checkbox-general').prop('checked', true);
        }else{
            $('.checkbox-general').prop('checked',false);
        }
    })

    // Check All
    $('#check_all').click(function(){
        if($(this).prop('checked') === true){
            $('.checkbox_all').prop('checked',true);
        }else{
            $('.checkbox_all').prop('checked',false);
        }
    })

    // Delete
    $('.deleteItem').click(function (event){
        event.preventDefault();
        if(confirm(CONFIRMATION_MSG) === true){
            $('#delete-form').prop('action',$(this).data('id')).submit();
        }
    })

    // Chang Status
    $('.statusItem').click(function (event){
        event.preventDefault();
        if(confirm(CONFIRMATION_MSG) === true){
            $('#status-form').prop('action',$(this).data('id')).submit();
        }
    })

    // Restore
    $('.restoreItem').click(function (event){
        event.preventDefault();
        if(confirm(CONFIRMATION_MSG) === true){
            $('#restore-form').prop('action',$(this).data('id')).submit();
        }
    })

    $('.btn-empty-trash').click(function (event){
        event.preventDefault();
        if (confirm(CONFIRMATION_MSG) === true){
            $('#empty-trash-form').prop('action',$(this).data('id')).submit();
        }
    })

    $('.confirm').click(function (){
        return(confirm(CONFIRMATION_MSG));
    })

});


